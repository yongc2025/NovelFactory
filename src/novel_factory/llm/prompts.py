def render_prompt(template: str, **kwargs) -> str:
    """渲染 prompt 模板，用 kwargs 填充占位符。

    支持 Python str.format() 语法，如 {variable}。
    未提供的变量保留原样。
    """
    try:
        return template.format(**kwargs)
    except KeyError:
        # 部分变量缺失时，逐个替换已提供的变量
        result = template
        for key, value in kwargs.items():
            placeholder = "{" + key + "}"
            result = result.replace(placeholder, str(value))
        return result
